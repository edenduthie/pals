### Name: staxlab
### Title: Place staggered labels on an axis
### Aliases: staxlab
### Keywords: misc

### ** Examples

 plot(rnorm(12),axes=FALSE)
 box()
 months<-c("January","February","March","April","May","June",
  "July","August","September","October","November","December")
 staxlab(1,1:12,months)



