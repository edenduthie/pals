### Name: pie3D
### Title: Display a 3D pie chart
### Aliases: pie3D
### Keywords: misc

### ** Examples

 pieval<-c(2,4,6,8)
 pielabels<-
  c("We hate\n pies","We oppose\n  pies","We don't\n  care","We just love pies")
 pie3D(pieval,radius=0.9,labels=pielabels,explode=0.1,main="3D PIE OPINIONS")



